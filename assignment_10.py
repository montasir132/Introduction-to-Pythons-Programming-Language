# if-elif-else ব্যবহার করে একজন ব্যক্তি নাবালক (Minor), তরুণ (Young) না বৃদ্ধ (Old) তা নির্ধারণের জন্য প্রোগ্রাম। |

# Pogram-

age = int(input("Enter your age: "));

if (age < 19):
    print("Minor");
elif (age < 61):
    print("Young");
else:
    print("Old");


